    private void btnSubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSubmitActionPerformed
        // TODO add your handling code here:
        ComponentDealerWorkRequest request = new ComponentDealerWorkRequest();

        request.setMessage(areaEquipments.getText());
        request.setSender(userAccount);
        request.setStatus("Sent");
        request.setRequestType("Equipment Request");

        Enterprise ent = null;
        for(Enterprise enterprise : network.getEnterpriseDirectory().getEnterpriseList())
        {
            if(enterprise instanceof ComponentDealerEnterprise)
            {
                ent = enterprise;
                break;
            }
        }
        Organization org = null;
        for (Organization organization : ent.getOrganizationDirectory().getOrganizationList()){
            if (organization instanceof ComponentDealerOrganization){
                org = organization;
                break;
            }
        }
        if (org!=null){
            org.getWorkQueue().getWorkRequestList().add(request);
            userAccount.getWorkQueue().getWorkRequestList().add(request);
        }

        container.remove(this);
        CardLayout layout = (CardLayout) container.getLayout();
        layout.previous(container);
    }//GEN-LAST:event_btnSubmitActionPerformed


